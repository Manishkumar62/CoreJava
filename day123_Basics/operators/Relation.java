package com.day123_Basics.operators;

public class Relation {

	public static void main(String[] args) {
		int a=50,b=10;
		System.out.println("Equal to "+(a==b));
		System.out.println("Not equal two "+(a!=b));
		System.out.println("Greater than equal to "+(a>=b));
		System.out.println("Less then equal to "+(a<=b));
		System.out.println("Greater then "+(a>b));
		System.out.println("Less then "+(a<b));

	}

}
